int	ft_str_is_uppercase(char *str)
{
	int i;
	int out;

	i = 0;
	out = 1;
	while (str[i])
	{
		if (str[i] >= 1 && str[i] <= 64)
		{
			out = 0;
			break ;
		}
		else if (str[i] >= 91)
		{
			out = 0;
			break ;
		}
		i++;
	}
	return (out);
}
