int	ft_strcmp(char *s1, char *s2)
{
	int out;
	int i;

	i = 0;
	out = 0;
	while (s1[i] != 0 || s2[i] != 0)
	{
		out = s1[i] - s2[i];
		if (out < 0)
		{
			out = -1;
			break ;
		}
		else if (out > 0)
		{
			out = 1;
			break ;
		}
		i++;
	}
	return (out);
}
