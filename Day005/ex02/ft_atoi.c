int	ft_atoi(char *str)
{
	int out;
	int sign;

	out = 0;
	sign = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == 43)
		str++;
	else if (*str == 45)
	{
		sign = -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		out = out * 10 + (*str - 48);
		str++;
	}
	return (out * sign);
}
