void	ft_putchar(char c);

void	change(char **f)
{
	int i;
	int j;

	i = 1;
	while (i <= 9)
	{
		j = 0;
		while (f[i][j] != '\0')
		{
			ft_putchar(f[i][j]);
			if (j != 8)
				ft_putchar(' ');
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
